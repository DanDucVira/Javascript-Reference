//Switch Statements

let text = "banana";

switch (text) {
    case "banana":
        console.log("banana are great");
        break;

    case "Apple":
        console.log("Apples are okay");
        break;
    case "avocado":
        console.log("avocados are amazing");
        break;
    default:
        console.log("What fruit is that");
        break;
}