let age = 29;

//loose comparison(different types can still be equal)

console.log(age == 29);
console.log(age == '29');

console.log(age != 29);
console.log(age != '29');





//strict comparison(different types cannot be equal)
console.log(age === 29);
console.log(age === '29');


console.log(age !== 29);
console.log(age !== '29');